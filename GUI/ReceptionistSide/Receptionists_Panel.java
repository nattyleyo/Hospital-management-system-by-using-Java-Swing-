    private void btn_checkInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_checkInActionPerformed
        // TODO add your handling code here:
        JTextField inputTextField=new JTextField();
        int result;
        do{
            result= JOptionPane.showOptionDialog(this, new Object[] {"Enter the id of the patient you want\n to check in?", inputTextField},"Check In", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
            if(result==JOptionPane.CANCEL_OPTION){
                break;
            }
        }while(inputTextField.getText().length()==0);
        
        if(result==JOptionPane.OK_OPTION && inputTextField.getText().length()>=6){
            String patientId=inputTextField.getText();
            if(isNumeric(patientId.substring(1, 5))){
                int id=Integer.parseInt(patientId.substring(1, 5));
                try {
                    String url = "jdbc:mysql://localhost:3306/HMS_DB";
                    String username = "mahnat";
                    String password = "mahnat2023";
                    conn = DriverManager.getConnection(url, username, password);
                    pstmt=conn.prepareStatement("select patientId,checkInStatus from Patient where patientId=?");
                    pstmt.setInt(1, id);
                    rs=pstmt.executeQuery();
                    if(rs.next()){
                        boolean checkInStatus=rs.getBoolean(2);
                        if(checkInStatus==true){
                          JOptionPane.showMessageDialog(this, "Patient already checked in!!.", "Alert", JOptionPane.WARNING_MESSAGE);
                        }else{
                           pstmt=conn.prepareStatement("update Patient set checkInStatus=(?),bill=(?),checkInValue=(?)");
                           pstmt.setBoolean(1, true);
                           pstmt.setDouble(2, 200);
                           pstmt.setString(3, "Checked-In");
                           pstmt.executeUpdate();
                           JOptionPane.showMessageDialog(this, "Patient checked in successfully!!.", "Successfully Checked In", JOptionPane.INFORMATION_MESSAGE);
                           
                           rs.close();
                           pstmt.close();
                           conn.close();
                        }
                    }else{
                      JOptionPane.showMessageDialog(this, "Patient Id doesn't exist!!.", "Alert", JOptionPane.WARNING_MESSAGE);
                    }
                } catch (SQLException e) {
                    System.out.println(e);
                }  
            }else{
                      JOptionPane.showMessageDialog(this, "Inappropriate ID!!.", "Alert", JOptionPane.WARNING_MESSAGE);                
            }
        }else{
                      JOptionPane.showMessageDialog(this, "Inappropriate ID!!.", "Alert", JOptionPane.WARNING_MESSAGE);                            
        }
    }//GEN-LAST:event_btn_checkInActionPerformed
